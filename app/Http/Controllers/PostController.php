    public function comment(Request $request, Post $post)
    {
        $data = $request->validate([
            'content' => 'required|min:10|max:250'
        ]);

        Comment::create([
            'user_id' => auth()->user()->id,
            'post_id' => $post->id,
            'content' => $data['content'],
        ]);

        return redirect()->route('post.show', $post);
    }
